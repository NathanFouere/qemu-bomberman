void set_palette_vga(unsigned char palette_vga[256][3]) {
    // SÃ©lection de l'entrÃ©e 0
    outb(0x3C8, 0);
    // Envoi des 256 triplets VGA (6-bit)
    for(int i = 0; i < 256; i++) {
        outb(0x3C9, palette_vga[i][0]);
        outb(0x3C9, palette_vga[i][1]);
        outb(0x3C9, palette_vga[i][2]);
    }
}
// Draw a wÃ—h sprite at (dstX, dstY), skipping color 255
void draw_sprite(const unsigned char* sprite,
                 int w, int h,
                 int dstX, int dstY)
{
    for (int yy = 0; yy < h; ++yy) {
        for (int xx = 0; xx < w; ++xx) {
            unsigned char c = sprite[yy * w + xx];
            if (c != 0) {
                frame_buffer[(yy+dstY)*320 + (xx + dstX)] = c;
            }
        }
    }
}

unsigned char reverse_bits(unsigned char b) {
    b = (b & 0xF0) >> 4 | (b & 0x0F) << 4;
    b = (b & 0xCC) >> 2 | (b & 0x33) << 2;
    b = (b & 0xAA) >> 1 | (b & 0x55) << 1;
    return b;
}

void draw_char(char c, int x, int y, unsigned char color) {
    for (int row = 0; row < 8; row++) {
        unsigned char line = reverse_bits(font8x8_basic[(int)c][row]);
        for (int col = 0; col < 8; col++) {
            if (line & (1 << col)) {
                frame_buffer[(y + row) * 320 + (x + (7 - col))] = color;
            }
        }
    }
}

void draw_text(const char* str, int x, int y, unsigned char color) {
    while (*str) {
        draw_char(*str, x, y, color);
        x += 8;
        str++;
    }
}

void draw_number(unsigned int number, int x, int y, unsigned char color) {
    char str[12];
    itoa(number, str, 10);

    int length = 0;
    char* temp = str;
    while (*temp++) {
        length++;
    }
    int total_width = length * 8;

    for (int i = length - 1; i >= 0; i--) {
        x -= 8;
        draw_char(str[i], x, y, color);
    }
}
